    $(function(){
        if(!$("#slider")){
            return;
        }
        var current = 0;
        var length = $(".slider-wrapper").find("a").length;

        var play = function(num){
            $(".slider-wrapper").find("a").fadeOut();
            $(".slider-wrapper").find("a").eq(num).fadeIn();
            $(".slider-anchor").find("li").removeClass("active");
            $(".slider-anchor").find("li").eq(num).addClass("active");
            var title = $(".slider-anchor").find("li").eq(num).find(".slider-title").text();
            $(".slider-anchor-title").text(title);
        };
        play(0);
        var next = function(){
            if(current == length-1){
                current = 0;
            }else{
                current = current+1;
            }
            play(current);
        };

        var  prev = function(){
            if(current == 0){
                current = length-1;
            }else{
                current = current -1;
            }
            play(current);
        };

        for(var i=0;i<length;i++){
            (function(i){
                $(".slider-anchor").find("li").eq(i).on("click",function(){
                    play(i);
                })
            })(i)
        }

        $(".slider-right").on("click",function(){
            prev();
        })
        $(".slider-left").on("click",function(){
            next();
        })

        setInterval(function(){
            next();
        },8000);


        //首页点击量统计
        var IndexPageHitDom = $('#index-page-hit');
        if (IndexPageHitDom[0]){
          $.ajax({
            url:"index.php?m=uestcvideo&c=index&a=IndexPageHits",
            success:function(sdata){
              IndexPageHitDom.text(sdata);
            }
          });
        }
    })
